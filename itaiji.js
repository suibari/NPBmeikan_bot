//検索語に異体字のバリエーションを作成し、配列で返す
function variation(ts){
  var fx = [];
  var fy = [];
  var it = [];
  
  /* 異体字の配列に検索語を入れる */
	fx.push(ts);
	/* 検索語に異体字が存在する文字があるかを探し、あったら配列 it に入れる */
	for(let i = 0; i < ts.length; i++){
		/* 対象の文字が既に抽出済み異体字リストに登録されているか */
		q = 1;
		for (let j = 0; j < it.length; j++){
			if (ts[i] == it[j][0]){
				q = 0;
				break;
			}
		}
		/* 登録されていれば次の文字へ */
		if (q = 0) continue;
		/* 対象の文字が異体字リストにあるか探して抽出する */
		for(let j = 0; j < o.length; j++){
			if(ts[i] == o[j][0]){
				it.push(o[j]);
				break;
			}
		}
  }
	/* 検索語の異体字のバリエーションを作成 */
	for(let i = 0; i < it.length; i++){
		/* 検索語の配列を複製、複製した配列に対して処理する */
    fy = fx.concat();
		for(let j = 0; j < it[i][1].length; j++){
			for(let q = 0; q < fy.length; q++){
        /* 検索語を異体字に置換して元の配列に追加 */
        fx.push(fy[q].replace(new RegExp(it[i][0], "g"), it[i][1][j]));
			}
		}
	}
	return fx;
}
